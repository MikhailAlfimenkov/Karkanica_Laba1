public class TwittC {
    public double coordinateX;
    public double coordinateY;
    public String text;
    public double score;

    public TwittC() {
    }

    public TwittC(double coordinateX, double coordinateY, String text, double score) {
        this.coordinateX = coordinateX;
        this.coordinateY = coordinateY;
        this.text = text;
        this.score = score;
    }
}
